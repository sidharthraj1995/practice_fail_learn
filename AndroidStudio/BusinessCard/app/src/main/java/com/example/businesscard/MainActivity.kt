class Person(
    name: String,
    title: String,
    phone: Int,
    social: String,
    email: String
) {
    val name = name.replaceFirstChar { it.uppercase() }
    val title = title.replaceFirstChar { it.uppercase() }
    val phone = phone
    val social = "@$social"
    val email = email
}